package calculator;

public class Calculator {

    public int addition (int a, int b){
        return a + b;
    }

    public int subtraction (int a,int b){
        return a-b;
    }

    public double multiplication (int a,int b){
        return a*b;
    }

    public float division (int a,int b){
        return a/b;
    }
}
